/**
 * 从完整的章节字符串中提取出"单元核心名"，用于模糊匹配。
 * 输入: "第五单元 简易方程"      →  输出: "简易方程"
 * 输入: "1 准备课"                →  输出: "准备课"
 * 输入: "1 大数的认识"            →  输出: "大数的认识"
 * 输入: "8 数学广角——优化"        →  输出: "优化"
 * 输入: "1 5以内数的认识和加减法" →  输出: "5以内数的认识和加减法"
 * 输入: "数学游戏"                →  输出: "数学游戏"
 * 输入: "六 复习与关联"           →  输出: "复习与关联"
 */
function extractUnitCore(unit: string): string {
  let s = unit.trim();
  // 去掉开头的「第N单元」
  s = s.replace(/^第[一二三四五六七八九十0-9]+单元[\s:：、.．\-]*/, '');
  // 去掉开头的「N 」「N、」「N.」「N:」「N-」等编号
  s = s.replace(/^[0-9]+[\s:：、.．\-]*/, '');
  // 去掉开头的「一 」「二 」等中文大写数字+空格
  s = s.replace(/^[一二三四五六七八九十]+[\s:：、.．\-]*/, '');
  // 去掉"数学广角——"/"数学广角—"等修饰前缀
  s = s.replace(/^数学广角[—\-—]+/, '');
  return s.trim();
}

/**
 * 在 UNIT_KNOWLEDGE_POINTS 中根据 unit 字符串做模糊匹配，返回最接近的知识点列表。
 * - 第 1 步：核心名精确匹配
 * - 第 2 步：核心名包含关系（任一方向）
 * - 第 3 步：原字符串包含关系（任一方向）
 * - 第 4 步：返回 null（让调用方回退到 SEMESTER_KNOWLEDGE_POINTS）
 */
function findUnitKPMatch(unit: string): string[] | null {
  const core = extractUnitCore(unit);
  // 1) 核心名精确匹配
  if (core && UNIT_KNOWLEDGE_POINTS[core]) return UNIT_KNOWLEDGE_POINTS[core];
  // 2) 任一 key 包含核心名 或 核心名包含 key
  if (core) {
    for (const [key, value] of Object.entries(UNIT_KNOWLEDGE_POINTS)) {
      if (key.includes(core) || core.includes(key)) {
        return value;
      }
    }
  }
  // 3) 原字符串包含关系
  for (const [key, value] of Object.entries(UNIT_KNOWLEDGE_POINTS)) {
    if (unit.includes(key) || key.includes(unit)) {
      return value;
    }
  }
  return null;
}

/**
 * 公开 API：根据「年级+学期+unit 字符串」获取推荐知识点。
 * 找不到时返回 null，调用方应回退到 SEMESTER_KNOWLEDGE_POINTS。
 */
export function getKPsByUnit(
  _grade: string,
  _semester: string,
  unit: string
): string[] | null {
  if (!unit) return null;
  return findUnitKPMatch(unit);
}
